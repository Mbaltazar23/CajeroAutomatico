    private void jBtn_IngresarGiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_IngresarGiroActionPerformed
        Cliente cliente;
        try {
            Integer.parseInt(ClaveCli.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La Clave no es correcto");
        }

        if (RutCliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El rut no puede quedar vacio");
        } else if (ClaveCli.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "La clave no puede quedar vacio");
        } else {
            cliente = ControladorBanco.BuscarCliente(RutCliente.getText());
            if (cliente != null) {
                JOptionPane.showMessageDialog(this, cliente.getCli_saldo());
            } else {
                JOptionPane.showMessageDialog(this, "Saldo no disponible");
            }
        }
    }//GEN-LAST:event_jBtn_IngresarGiroActionPerformed

